/**
 * normalize.js — 読み上げ(TTS)最適化のためのテキスト正規化。
 *
 * 方針:
 *  - Markdown 装飾記号(** * ` # > 箇条書き/番号)を除去。
 *  - 括弧書き（…）(…)は中身ごと削除（「」『』等の引用は保持）。
 *  - ハイフン/ダッシュを除去（語中は空白化）。ただしカタカナ長音符 ー(U+30FC) は保持。
 *  - 半角スペース: 日本語(CJK)隣接は削除、英単語間は1つ残す。全角スペースは削除。
 *  - 句点「。」(丸)は読点「、」(点)へ置換。読み上げの長い間を短い間にする。
 *
 * 破壊的変換なので、コードを含めたい RAW モードでは呼ばない想定。
 */

// CJK(ひらがな/カタカナ/漢字/全角記号)範囲。長音符 ー(U+30FC) もこの範囲だが
// 除去対象の「空白」「ハイフン」とは別物なので影響しない。
const CJK = "\\u3040-\\u30ff\\u3400-\\u4dbf\\u4e00-\\u9fff\\uff00-\\uffef";
const SPACE_BEFORE_CJK = new RegExp("[ \\t]+(?=[" + CJK + "])", "g");
const SPACE_AFTER_CJK = new RegExp("(?<=[" + CJK + "])[ \\t]+", "g");

function normalizeForSpeech(input) {
  let t = input;

  // 1) インライン装飾
  t = t.replace(/`+/g, "");                 // インラインコードのバッククォート
  // Markdown リンク [表示テキスト](url) → 表示テキスト（括弧除去より前に）
  t = t.replace(/\[([^\]]+)\]\([^)]*\)/g, "$1");
  t = t.replace(/\*\*([^*]+)\*\*/g, "$1");  // 太字
  t = t.replace(/\*([^*]+)\*/g, "$1");      // 斜体(*)
  t = t.replace(/(^|[^\w])_([^_\n]+)_(?=[^\w]|$)/g, "$1$2"); // 斜体(_)
  t = t.replace(/[*]/g, "");                // 残った *
  t = t.replace(/[\[\]]/g, "");             // 残った半角角括弧([])。「」【】は保持

  // 2) 括弧書きの除去（全角・半角）。入れ子に備えて数回適用。
  for (let i = 0; i < 3; i++) {
    t = t.replace(/（[^（）]*）/g, "");
    t = t.replace(/\([^()]*\)/g, "");
  }

  // 3) ダッシュ/ハイフン
  t = t.replace(/[—–―]/g, "");                          // em/en/horizontal bar
  t = t.replace(/([0-9A-Za-z])-([0-9A-Za-z])/g, "$1 $2"); // 語中ハイフン→空白

  // 3.5) 矢印・スラッシュ（読み上げ不要）。区切り用途は読点に、それ以外は削除。
  t = t.replace(/\s*(?:→|←|↑|↓|⇒|⇐|⇔|⟶|➡|=>|->)\s*/g, "、"); // 矢印→読点
  t = t.replace(/\s+[／/]\s+/g, "、"); // 空白付きスラッシュ(区切り)→読点
  t = t.replace(/[／/]/g, "");         // 残りのスラッシュは削除

  // 4) 行単位: 行頭マーカー除去（長音符は無関係）
  t = t
    .split(/\r?\n/)
    .map((line) => {
      let s = line;
      // 表の区切り行(|---|:--:|等)は丸ごと削除
      if (/\|/.test(s) && /^[\s|:\-]+$/.test(s) && /-/.test(s)) return "";
      // 表のセル行: 前後のパイプを除き、セル区切り | を読点に
      if (/\|/.test(s)) {
        s = s.replace(/^\s*\|/, "").replace(/\|\s*$/, "");
        s = s.replace(/\s*\|\s*/g, "、");
      }
      s = s.replace(/^\s*#{1,6}\s*/, "");   // 見出し
      s = s.replace(/^\s*>\s?/, "");        // 引用
      s = s.replace(/^\s*[-*+]\s+/, "");    // 箇条書き
      s = s.replace(/^\s*\d+[.)]\s+/, "");  // 番号リスト
      return s;
    })
    .join("\n");

  // 5) 残りのハイフン(U+002D)を削除。長音符 ー(U+30FC) は対象外。
  t = t.replace(/-/g, "");

  // 6) 空白整理
  t = t.replace(/　/g, "");      // 全角スペース削除
  t = t.replace(/[ \t]+/g, " ");     // 連続半角スペースを1つに
  t = t.replace(SPACE_BEFORE_CJK, ""); // CJK 直前の半角スペース削除
  t = t.replace(SPACE_AFTER_CJK, "");  // CJK 直後の半角スペース削除

  // 6.5) 句読点の整理（矢印/スラッシュ置換で生じた重複読点など）
  t = t.replace(/。/g, "、");                   // 句点(丸)→読点(点): 長い間を短い間に
  t = t.replace(/、[、\s]*、/g, "、");          // 連続読点を1つに
  t = t.replace(/、(?=[。、）」』】])/g, "");     // 文末・閉じ括弧直前の読点は不要

  // 7) 行端の空白と過剰な空行を整理
  t = t
    .split(/\r?\n/)
    .map((l) => l.trim().replace(/^[、]+/, "")) // 行頭の読点を除去

    .filter((l, i, arr) => !(l === "" && arr[i - 1] === "")) // 連続空行を圧縮
    .join("\n")
    .replace(/\n{3,}/g, "\n\n")
    .trim();

  return t;
}

module.exports = { normalizeForSpeech };
